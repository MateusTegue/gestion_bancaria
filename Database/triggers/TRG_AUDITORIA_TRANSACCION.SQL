    AFTER INSERT OR UPDATE ON PROYECTODB.TBL_TRANSACCIONES
    FOR EACH ROW
DECLARE

BEGIN

    INSERT INTO PROYECTODB.TBL_AUDITORIA_TRANSACCIONES (
        AUDITORIA_ID,
        TRANSACCION_ID,
        CUENTA_ID,
        FECHA_OPERACION
